n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))
print('{}+{}= {}'.format(n1, n2, n1+n2))
print('{}-{}= {}'.format(n1, n2, n1-n2))
print('{}*{}= {}'.format(n1, n2, n1*n2))
print('{}/{}= {}'.format(n1, n2, n1/n2))
print('{}/{}= {:.3f}'.format(n1, n2, n1/n2))
print('{}**{}= {}'.format(n1, n2, n1**n2))
print('pow({},{})= {}'.format(n1, n2, pow(n1,n2)))
print('{}//{}= {}'.format(n1, n2, n1//n2))
print('{}%{}= {}'.format(n1, n2, n1%n2))