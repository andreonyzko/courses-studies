let x = 10, y = 3;
console.log("x = " + x);
console.log("y = " + y);

console.log("x+y = " + (x+y) );
console.log("x-y = " + (x-y) );
console.log("x*y = " + (x*y) );
console.log("x/y = " + (x/y) );
console.log("x%y = " + (x%y) );
console.log("x**y = " + (x**y) );

// Increment and decrement
console.log("++x = " + (++x) );
console.log("x++ = " + (x++) );
console.log("x = " + x );

console.log("--x = " + (--x) );
console.log("x-- = " + (x--) );
console.log("x = " + x );